def add(hank: int, judy: int) -> int:
    """
    this performs basic addition
    :param hank: int
    :param judy: int
    :return: + of judy and judy
    """
    return hank + judy

def mult(alice: int, bob: int) -> int:
    """
    this performs basic mult
    :param alice: int
    :param bob: int
    :return: * of alice and bob
    """
    return alice // bob
